#!/usr/bin/env node
// Regenerates web/src/config/generated.ts from contracts/deployments/*.json
// so public-chain deployments flow into the app without hand-editing.
// Usage: node scripts/sync-web-config.mjs [chainId…]  (default: all found)
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const depDir = join(root, "contracts", "deployments");
const outFile = join(root, "web", "src", "config", "generated.ts");

const LOCAL_CHAIN = "31337"; // local stays hardcoded in deployments.ts

const requested = process.argv.slice(2);
const chainIds = (
  requested.length
    ? requested
    : readdirSync(depDir)
        .filter((f) => /^\d+\.json$/.test(f))
        .map((f) => f.replace(".json", ""))
).filter((id) => id !== LOCAL_CHAIN);

const deployments = {};
const tokenLists = {};

for (const id of chainIds) {
  const dep = JSON.parse(readFileSync(join(depDir, `${id}.json`), "utf8"));
  deployments[id] = {
    weth: dep.weth,
    factory: dep.factory,
    router: dep.router,
    merryMenShare: dep.merryMenShare,
    lox: dep.lox,
    bowStaking: dep.bowStaking,
  };

  const list = [
    { address: "0x0000000000000000000000000000000000000000", symbol: "ETH", name: "Ether", decimals: 18, isNative: true },
    { address: dep.weth, symbol: "WETH", name: "Wrapped Ether", decimals: 18 },
    { address: dep.lox, symbol: "LOX", name: "Loxley", decimals: 18 },
  ];
  try {
    const tok = JSON.parse(readFileSync(join(depDir, `${id}.tokens.json`), "utf8"));
    list.push(
      { address: tok.gold, symbol: "GOLD", name: "Marian's Gold", decimals: 18 },
      { address: tok.silver, symbol: "SILV", name: "Sheriff's Silver", decimals: 6 },
      { address: tok.ale, symbol: "ALE", name: "Friar's Ale", decimals: 18 },
    );
  } catch {
    // no seeded tokens on this chain — ETH/WETH/LOX only
  }
  tokenLists[id] = list;
}

const body = `// ═══════════════════════════════════════════════════════════════════
// AUTO-GENERATED by scripts/sync-web-config.mjs — do not edit by hand.
// Holds deployments made to public chains; merged into deployments.ts.
// ═══════════════════════════════════════════════════════════════════
import type { Deployment, TokenInfo } from "./deployments";

export const GENERATED_DEPLOYMENTS: Record<number, Deployment> =
  ${JSON.stringify(deployments, null, 2)} as unknown as Record<number, Deployment>;

export const GENERATED_TOKEN_LISTS: Record<number, TokenInfo[]> =
  ${JSON.stringify(tokenLists, null, 2)} as unknown as Record<number, TokenInfo[]>;
`;

writeFileSync(outFile, body);
console.log(`wrote ${outFile} (chains: ${chainIds.join(", ") || "none"})`);
